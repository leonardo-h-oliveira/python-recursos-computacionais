def exemplo_tipos():
    inteiro = 10
    flutuante = 3.5
    texto = "Python"
    booleano = True

    print("Inteiro:", inteiro, type(inteiro))
    print("Flutuante:", flutuante, type(flutuante))
    print("Texto:", texto, type(texto))
    print("Booleano:", booleano, type(booleano))

if __name__ == "__main__":
    exemplo_tipos()
